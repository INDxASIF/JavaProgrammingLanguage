import java.util.*;

public class AssignmentOperators {
    public static void main(String[] args) {
        int A = 10;
        // A = A + 10;
        A += 100;
        System.out.println(A);
        int B = 20;
        B -= 10;
        System.out.println(B);
        int C = 20;
        C *= 10;
        System.out.println(C);
        int D = 20;
        D/= 10;
        System.out.println(D);
        int E = 20;
        E %= 7;
        System.out.println(E);
    }
}